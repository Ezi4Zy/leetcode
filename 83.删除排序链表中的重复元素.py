# [83] 删除排序链表中的重复元素
#

# @lc code=start
# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def deleteDuplicates(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        node = head
        while node:
            next_ = node.next
            while next_ and next_.val == node.val:
                next_ = next_.next
            node.next = next_
            node = next_
        return head            
# @lc code=end

